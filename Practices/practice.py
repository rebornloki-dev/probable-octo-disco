# LEARNING Goal: Variables, Data Types, and User Input/Output
# This script demonstrates basic Python concepts: string and integer variables,
# printing with multiple arguments, and taking user input.
# It teaches: variable assignment, type mixing in print(), and the input() function.

# Assign a string variable
name = "Jerry"
# Assign an integer variable
age= 12
# Print individual variables
print(name)
print(age)
# Print multiple values in one line using commas
print("My name is",name,"and i am",age) 
# Get user input and store it in a variable
name = input("What is your name")
# Print with the user-provided input
print("welcome",name)
# Get another user input
favgame = input ("Whats your favorite game")
# Print the response with string concatenation
print("Oh so you like",favgame,"!","Good Choice!")
